package com.techlab.inicio;

public class Clase3gruposdedatos {
    public static void main(String[] args) {
        String texoEj = "mensaje en formato String 123";
        System.out.println(texoEj.length());
        System.out.println(texoEj.charAt(4));
        System.out.println(texoEj.toUpperCase());
        texoEj = texoEj.toUpperCase();
        System.out.println(texoEj);
        System.out.println(texoEj.toLowerCase());
    }
}
